public class Arithmetic {
    public static void main(String[] args){
        int x = 10;
        int y = 4;

        System.out.println("x + Y = "+(x+y));
        System.out.println("x - Y = "+(x-y));
        System.out.println("x * Y = "+(x*y));
        System.out.println("x / Y = "+(x/y));
        System.out.println("x % Y = "+(x%y));
    }
}
